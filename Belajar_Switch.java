public class Belajar_Switch 
{
    public static void main(String[] args) {
        int pilihan;
        pilihan = 1;
        
        switch (pilihan)
        {
            case 1:
                System.out.println("STMIK Widya Utama");
                break;
                
            case 2:
                System.out.println("UGM");
                break; 
                
            case 3:
                System.out.println("UI");
                break; 
                
            default:
                System.out.println("Unnes ");
        }
    }
}
